print('\n**********Even or Odd number**********')

# Input integer from user and tell even or odd
user_input = int(input('Enter your number: '))

if user_input%2 == 0:   #condition to check if user input even number or odd number
    print('The number is even.')
else:                    #in all other cases, the number will be odd   
    print('The number is odd.')

